/*
    LAÇO FOR
    A declaração for, cria um loop, em que testa uma condição e executa um 
    determinado enquanto a condição satisfazer uma determinada condição
    pré estabelecido pelo programador.
*/

// exemplo prático 1
let msg = '';
for (let i = 0; i < 10; i++) {
    msg += i + ', ';
}
console.log(msg);
console.log(typeof(msg));
