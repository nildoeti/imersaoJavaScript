/*
	super

	A palavra-chave super é usada para acessar o objeto pai de um objeto, em
	outros casos, é usada para acessar a classe pai de uma classe.
	Síntaxe

	// chama o objeto (ou construtor) pai
	super(...[arguments]);
	// chama um método da classe/objeto pai
	super.metodo([arguments]);

	Descrição

	Quando usada no construtor de uma classe, a palavra-chave super deve ser
	usada apenas uma vez, e precisa ser usada antes que a palavra-chave this
	possa ser usada. Essa palavra-chave também pode ser usada para chamar uma
	função ou objeto pai.

	Fonte MDN.
	url(https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Operators/super)
*/

