/**
 *  Operação aritimética com true e false em JavaScript
 */

console.log(true + 0); // 1 + 0 = 1
console.log(true + true); // 1 + 1 = 2
console.log(true - true); // 1 - 1 = 0
console.log(true - false); // 1 - 0: 1
console.log(false + false); // 0 + 0 = 0
console.log(false - false); // 0 - 0 = 0
console.log(true * true); // 1 * 1 = 1
console.log(true * false); // 1 * 0 = 0
console.log(false * false); // 0 * 0 = 0
console.log(true / true); // 1 / 1 = 1
console.log(true / false); // Em JavaScript, qualquer número dividido por 0 é (Infinity)