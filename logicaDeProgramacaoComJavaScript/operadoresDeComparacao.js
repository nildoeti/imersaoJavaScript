/*
    OPERADORES DE COMPARAÇÃO
    > maior que
    >= maior que ou igual
    < menor que
    <= menor que ou igual
    == igualdade de valor
    === igualdade estrita de valor e tipo
    != diferente de valor
    !== diferente estrito de valor e tipo
*/

console.log(10 > 5); // true
console.log(10 >= 5); // true
console.log(10 < 5); // false
console.log(10 <= 5); // false
console.log(10 == 5); // false
console.log(10 === 5); // false
console.log(10 != 5); // true
console.log(10 !== 5); // true
