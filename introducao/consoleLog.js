/**
 * DEFINIÇÃO E USO
 * O método console.log(), é uma função do JavaScript, para exibir qualquer
 * tipo de conteúdo em sua saida padrão, sejam elas mensagens de textos,
 * numeros entre outros valores.
 */

// Imprimindo uma cadeia de caracteres diretamente no console
console.log('Eu sou um texto.');

// Exibindo variável do tipo string, como parâmetro no console
let cor = 'amarelo';
console.log(cor);

// Exibindo variável do tipo inteiro, como parâmetro no console
let ano = 2000;
console.log(ano);

// exibindo vários valores diferentes no console com uso da vírgula
console.log(35, 3.45, 'cadeia de caracteres')
