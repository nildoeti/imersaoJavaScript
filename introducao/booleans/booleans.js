/**
 * Valores boobleans retornan sempre valors do 
 * tipo true (verdade) ou false (falso)
 */

let valorUm = 4;
let valorDois = 2;

let primeiraComparacao  = valorUm == valorDois;
console.log(primeiraComparacao); // false

let segundaComparacao = valorUm > valorDois;
console.log(segundaComparacao); // true